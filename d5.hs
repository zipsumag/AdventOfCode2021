data Point = Point Int Int deriving (Eq, Show)

instance Ord Point where
  compare (Point x1 y1) (Point x2 y2) = (x1, y1) `compare` (x2, y2)

data Segment = Segment Point Point deriving (Eq, Show)

makeSegment :: [Int] -> Segment
makeSegment (x1:y1:x2:y2:[]) = Segment (Point x1 y1) (Point x2 y2)

isVert :: Segment -> Bool
isVert (Segment (Point x1 _) (Point x2 _)) = x1 == x2

isHor :: Segment -> Bool
isHor (Segment (Point _ y1) (Point _ y2)) = y1 == y2

getSegmentPointsHV :: Segment -> [Point]
getSegmentPointsHV s@(Segment (Point x1 y1) (Point x2 y2))
  | isVert s  = [Point x1 y | y <- [(min y1 y2)..(max y1 y2)]]
  | isHor s   = [Point x y1 | x <- [(min x1 x2)..(max x1 x2)]]
  | otherwise = []

getSegmentPointsDiag :: Segment -> [Point]
getSegmentPointsDiag s@(Segment (Point x1 y1) (Point x2 y2))
  | not (isVert s || isHor s) = [Point x y | x <- getBetween x1 x2 | y <- getBetween y1 y2]
  | otherwise                 = []

